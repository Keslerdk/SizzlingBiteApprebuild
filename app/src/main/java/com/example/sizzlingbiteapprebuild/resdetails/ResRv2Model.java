package com.example.sizzlingbiteapprebuild.resdetails;

public class ResRv2Model {
    private String name;
    private int image;

    public ResRv2Model(String name, int image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }

}
